import Head from 'next/head'
import { Vendors } from '../api/types';
import { getVendors } from '../api/vendors';
import Main from '../components/main';
import { vendorsSort } from '../helper/util';

interface HomeProps {
  initVendors: Vendors
}

export default function Home({ initVendors }: HomeProps) {
  return (
    <>
      <Head>
        <title>Vendors</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Main initVendors={initVendors} />
    </>
  )
}

// Get initial vendors (lazy load the rest client side)
export async function getStaticProps() {
  let vendors: Vendors | Error; 
  try {
    // Future case: have users sort this data so no need to FE sort it
    vendors = await getVendors<Vendors>(14);
    vendors.Items = vendorsSort(vendors.Items);
  } catch (e) {
    if (e instanceof Error) {
      vendors = { Items: [], count: 0, lastEvaluatedKey: null }
    } else {
      throw new Error('getVendors unexpected error');   
    }
  }
  
  return { 
    props: {
      initVendors: vendors
    },
    revalidate: 60, // seconds
  }
}
